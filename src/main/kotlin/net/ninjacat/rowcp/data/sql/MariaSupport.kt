package net.ninjacat.rowcp.data.sql

import net.ninjacat.rowcp.data.Column

class MariaSupport : SqlSupport {
    override fun insertIgnoringQuery(tableName: String, columns: List<Column>): String {
        return "INSERT IGNORE INTO $tableName(${columns.joinToString(",") { it.name }})\n" +
                "VALUES(${columns.joinToString(",") { "?" }})"
    }
}